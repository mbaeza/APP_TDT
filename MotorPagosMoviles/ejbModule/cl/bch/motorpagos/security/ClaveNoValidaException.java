package cl.bch.motorpagos.security;

public class ClaveNoValidaException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -7110437065053773461L;
	
	public final String mensaje;
	
	/**
	 * @return the mensaje
	 */
	public String getMensaje() {
		return mensaje;
	}

	/**
	 * 
	 * @param str
	 */
	public ClaveNoValidaException(String str){
		this.mensaje=str;
	}

}
