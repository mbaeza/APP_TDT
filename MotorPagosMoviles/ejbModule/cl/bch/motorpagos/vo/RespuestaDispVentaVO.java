package cl.bch.motorpagos.vo;

import java.io.Serializable;

/**
 * @author boyanedel
 *
 */
public class RespuestaDispVentaVO implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 3983694681880260427L;
	private RespuestaVO retorno;
	private String idDispositivo;
	private String idConvenio;
	private String idCuenta;
	private String razonSocial;
	private String direccion;
	private String avatar;
	private String nombreConvenio;
	
	/**
	 * @return the retorno
	 */
	public RespuestaVO getRetorno() {
		return retorno;
	}
	/**
	 * @param retorno the retorno to set
	 */
	public void setRetorno(RespuestaVO retorno) {
		this.retorno = retorno;
	}
	/**
	 * @return the idDispositivo
	 */
	public String getIdDispositivo() {
		return idDispositivo;
	}
	/**
	 * @param idDispositivo the idDispositivo to set
	 */
	public void setIdDispositivo(String idDispositivo) {
		this.idDispositivo = idDispositivo;
	}
	/**
	 * @return the idConvenio
	 */
	public String getIdConvenio() {
		return idConvenio;
	}
	/**
	 * @param idConvenio the idConvenio to set
	 */
	public void setIdConvenio(String idConvenio) {
		this.idConvenio = idConvenio;
	}
	/**
	 * @return the idCuenta
	 */
	public String getIdCuenta() {
		return idCuenta;
	}
	/**
	 * @param idCuenta the idCuenta to set
	 */
	public void setIdCuenta(String idCuenta) {
		this.idCuenta = idCuenta;
	}
	/**
	 * @return the razonSocial
	 */
	public String getRazonSocial() {
		return razonSocial;
	}
	/**
	 * @param razonSocial the razonSocial to set
	 */
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}
	/**
	 * @return the direccion
	 */
	public String getDireccion() {
		return direccion;
	}
	/**
	 * @param direccion the direccion to set
	 */
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}	
	/**
	 * @return the avatar
	 */
	public String getAvatar() {
		return avatar;
	}
	/**
	 * @param avatar the avatar to set
	 */
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	
	/**
	 * @return the nombreConvenio
	 */
	public String getNombreConvenio() {
		return nombreConvenio;
	}
	/**
	 * @param nombreConvenio the nombreConvenio to set
	 */
	public void setNombreConvenio(String nombreConvenio) {
		this.nombreConvenio = nombreConvenio;
	}
	@Override
	public String toString() {
		StringBuffer bf = new StringBuffer(210);
		
		bf.append(this.retorno.toString());
		bf.append(", IdDispositivo : ");
		bf.append(this.idDispositivo);
		bf.append(", IdConvenio : ");
		bf.append(this.idConvenio);
		bf.append(", IdCuenta : ");
		bf.append(this.idCuenta);
		bf.append(", RazonSocial : ");
		bf.append(this.razonSocial);
		bf.append(", NombreConvenio : ");
		bf.append(this.nombreConvenio);
		bf.append(", Direccion : ");
		bf.append(this.direccion);
				
		return bf.toString();
	}
}
