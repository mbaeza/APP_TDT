package cl.bch.motorpagos.vo;

import java.io.Serializable;

/**
 * @author boyanedel
 *
 */
public class RespuestaPreCobroVO implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -378402807768158517L;
	private RespuestaVO retorno;
	private String nombreComercio;	
	private String direccionComercio;
	private String glosaOperacion;
	private int montoOperacion;
	private int montoPropina;
	private int subTotalTrx;
	private String rutComercio;
	private String idConvenio;
	private String avatar;
	private String tipoComercio;
	/**
	 * @return the retorno
	 */
	public RespuestaVO getRetorno() {
		return retorno;
	}
	/**
	 * @param retorno the retorno to set
	 */
	public void setRetorno(RespuestaVO retorno) {
		this.retorno = retorno;
	}
	/**
	 * @return the nombreComercio
	 */
	public String getNombreComercio() {
		return nombreComercio;
	}
	/**
	 * @param nombreComercio the nombreComercio to set
	 */
	public void setNombreComercio(String nombreComercio) {
		this.nombreComercio = nombreComercio;
	}
	
	/**
	 * @return the glosaOperacion
	 */
	public String getGlosaOperacion() {
		return glosaOperacion;
	}
	/**
	 * @param glosaOperacion the glosaOperacion to set
	 */
	public void setGlosaOperacion(String glosaOperacion) {
		this.glosaOperacion = glosaOperacion;
	}
	/**
	 * @return the direccionComercio
	 */
	public String getDireccionComercio() {
		return direccionComercio;
	}
	/**
	 * @param direccionComercio the direccionComercio to set
	 */
	public void setDireccionComercio(String direccionComercio) {
		this.direccionComercio = direccionComercio;
	}
	/**
	 * @return the montoOperacion
	 */
	public int getMontoOperacion() {
		return montoOperacion;
	}
	/**
	 * @param montoOperacion the montoOperacion to set
	 */
	public void setMontoOperacion(int montoOperacion) {
		this.montoOperacion = montoOperacion;
	}	
	/**
	 * @return the rutComercio
	 */
	public String getRutComercio() {
		return rutComercio;
	}
	/**
	 * @param rutComercio the rutComercio to set
	 */
	public void setRutComercio(String rutComercio) {
		this.rutComercio = rutComercio.toUpperCase();
	}	
	/**
	 * @return the idConvenio
	 */
	public String getIdConvenio() {
		return idConvenio;
	}
	/**
	 * @param idConvenio the idConvenio to set
	 */
	public void setIdConvenio(String idConvenio) {
		this.idConvenio = idConvenio;
	}
	/**
	 * @return the avatar
	 */
	public String getAvatar() {
		return avatar;
	}
	/**
	 * @param avatar the avatar to set
	 */
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	/**
	 * @return the tipoComercio
	 */
	public String getTipoComercio() {
		return tipoComercio;
	}
	/**
	 * @param tipoComercio the tipoComercio to set
	 */
	public void setTipoComercio(String tipoComercio) {
		this.tipoComercio = tipoComercio;
	}	
	/**
	 * @return the montoPropina
	 */
	public int getMontoPropina() {
		return montoPropina;
	}
	/**
	 * @param montoPropina the montoPropina to set
	 */
	public void setMontoPropina(int montoPropina) {
		this.montoPropina = montoPropina;
	}
	
	/**
	 * @return the subTotalTrx
	 */
	public int getSubTotalTrx() {
		return subTotalTrx;
	}
	/**
	 * @param subTotalTrx the subTotalTrx to set
	 */
	public void setSubTotalTrx(int subTotalTrx) {
		this.subTotalTrx = subTotalTrx;
	}
	@Override
	public String toString() {
		StringBuffer bf = new StringBuffer(240);
		
		bf.append(this.retorno.toString());
		bf.append(", nombreComercio : ");
		bf.append(this.nombreComercio);
		bf.append(", glosaOperacion : ");
		bf.append(this.glosaOperacion);
		bf.append(", direccionComercio : ");
		bf.append(this.direccionComercio);
		bf.append(", montoPropina : ");
		bf.append(this.montoPropina);
		bf.append(", subTotalTrx : ");
		bf.append(this.subTotalTrx);
		bf.append(", montoOperacion : ");
		bf.append(this.montoOperacion);
		bf.append(", rutComercio : ");
		bf.append(this.rutComercio);		
		bf.append(", IdConvenio : ");
		bf.append(this.idConvenio);	
		bf.append(", TipoComercio : ");
		bf.append(this.tipoComercio);	
				
		return bf.toString();
	}
}
